import '../../domain/entities/chargement_entity.dart';

class ChargementModel extends ChargementEntity {
  const ChargementModel({
    required super.numeroFiche,
    required super.bundleId,
    required super.campagne,
    required super.sticker,
    required super.status,
    required super.typeTransfert,
    super.date,
    super.region,
    super.departement,
    super.sousPrefecture,
    super.village,
    super.destinationVille,
    super.destinateur,
    super.acheteur,
    super.contactAcheteur,
    super.codeAcheteur,
    super.nomMagasin,
    super.denomination,
    super.thDepart,
    super.sacs,
    super.poids,
    super.nomTransporteur,
    super.contactTransporteur,
    super.marqueCamion,
    super.immatriculation,
    super.remorque,
    super.avantCamion,
    super.nomChauffeur,
    super.permisConduire,
    super.prix,
    super.image,
    // Destination
    super.destDateDechargement,
    super.destHeure,
    super.destNomExportateur,
    super.destCodeExportateur,
    super.destPortUsineDechargement,
    super.destPontBascule,
    super.destNomMagasin,
    super.destKor,
    super.destNombreSacsDecharges,
    super.destNombreSacsRembourses,
    super.destTauxHumidite,
    super.destPoidsBrut,
    super.destTare,
    super.destPoidsNet,
    super.destPrixKg,
    super.destTauxDefectueux,
    super.destGrainage,
    super.destObservations,
  });

  factory ChargementModel.fromJson(Map<dynamic, dynamic> json) {
    return ChargementModel(
      numeroFiche: json['numeroFiche'] as dynamic,
      bundleId: json['bundle_id'] as dynamic,
      campagne: json['campagne'] as dynamic ?? '2025-2026',
      sticker: json['sticker'] as dynamic ?? '',
      status: json['status'] as dynamic ?? 'PENDING',
      typeTransfert: json['typeTransfert'] as dynamic ?? '2',
      date: json['date'] as dynamic,
      region: json['region'] as dynamic,
      departement: json['departement'] as dynamic,
      sousPrefecture: json['sousPrefecture'] as dynamic,
      village: json['village'] as dynamic,
      destinationVille: json['destinationVille'] as dynamic,
      destinateur: json['destinateur'] as dynamic,
      acheteur: json['acheteur'] as dynamic,
      contactAcheteur: json['contactAcheteur'] as dynamic,
      codeAcheteur: json['codeAcheteur'] as dynamic,
      nomMagasin: json['nomMagasin'] as dynamic,
      denomination: json['denomination'] as dynamic,
      thDepart: json['thDepart'] as dynamic,
      sacs: json['sacs'] as dynamic,
      poids: json['poids'] as dynamic,
      nomTransporteur: json['nomTransporteur'] as dynamic,
      contactTransporteur: json['contactTransporteur'] as dynamic,
      marqueCamion: json['marqueCamion'] as dynamic,
      immatriculation: json['immatriculation'] as dynamic,
      remorque: json['remorque'] as dynamic,
      avantCamion: json['avantCamion'] as dynamic,
      nomChauffeur: json['nomChauffeur'] as dynamic,
      permisConduire: json['permisConduire'] as dynamic,
      prix: json['prix'] as dynamic,
      image: json['image'] as dynamic,
      // Destination Fields
      destDateDechargement: json['dest_date_dechargement'] as dynamic,
      destHeure: json['dest_heure'] as dynamic,
      destNomExportateur: json['dest_nom_exportateur'] as dynamic,
      destCodeExportateur: json['dest_code_exportateur'] as dynamic,
      destPortUsineDechargement:
          json['dest_port_usine_dechargement'] as dynamic,
      destPontBascule: json['dest_pont_bascule'] as dynamic,
      destNomMagasin: json['dest_nom_magasin'] as dynamic,
      destKor: json['dest_kor'] as dynamic,
      destNombreSacsDecharges: json['dest_nombre_sacs_decharges'] as num?,
      destNombreSacsRembourses: json['dest_nombre_sacs_rembourses'] as num?,
      destTauxHumidite: json['dest_taux_humidite'] as num?,
      destPoidsBrut: json['dest_poids_brut'] as num?,
      destTare: json['dest_tare'] as num?,
      destPoidsNet: json['dest_poids_net'] as num?,
      destPrixKg: json['dest_prix_kg'] as num?,
      destTauxDefectueux: json['dest_taux_defectueux'] as num?,
      destGrainage: json['dest_grainage'] as num?,
      destObservations: json['dest_observations'] as dynamic,
    );
  }
}
